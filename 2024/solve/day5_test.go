package solve

import "testing"

func Test5(t *testing.T) {
    input := []string{
        "47|53",
        "97|13",
        "97|61",
        "97|47",
        "75|29",
        "61|13",
        "75|53",
        "29|13",
        "97|29",
        "53|29",
        "61|53",
        "97|53",
        "61|29",
        "47|13",
        "75|47",
        "97|75",
        "47|61",
        "75|61",
        "47|29",
        "75|13",
        "53|13",
        "",
        "75,47,61,53,29",
        "97,61,53,29,13",
        "75,29,13",
        "75,97,47,61,53",
        "61,13,29",
        "97,13,75,29,47",
    }

	p1, p2, err := Run5(input)
	if err != nil {
		t.Fatalf(err.Error())
	}

	if p1 != 143 {
		t.Logf("Part 1: want 143, got %d", p1)
		t.Fail()
	}

	if p2 != 123 {
		t.Logf("Part 2: want 123, got %d", p2)
		t.Fail()
	}
}
